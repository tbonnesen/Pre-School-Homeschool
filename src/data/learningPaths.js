export function getWeekProgress(path, weekIndex, completed) {
  const week = path.weeks[weekIndex];
  if (!week) return { done: 0, total: 0 };
  let done = 0, total = 0;
  for (const [domain, acts] of Object.entries(week.activities)) {
    for (const act of acts) {
      total++;
      if (completed?.[domain]?.[act.id]) done++;
    }
  }
  return { done, total };
}

export function getPathProgress(path, completed) {
  let done = 0, total = 0;
  for (let i = 0; i < path.weeks.length; i++) {
    const wp = getWeekProgress(path, i, completed);
    done += wp.done;
    total += wp.total;
  }
  return { done, total, percent: total > 0 ? Math.round(done / total * 100) : 0 };
}

export function getCurrentWeek(path, completed) {
  // Return the first incomplete week, or last week if all done
  for (let i = 0; i < path.weeks.length; i++) {
    const { done, total } = getWeekProgress(path, i, completed);
    if (done < total) return i;
  }
  return path.weeks.length - 1;
}
