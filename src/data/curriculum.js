export function getActivityById(domainId, activityId) {
  const activities = ACTIVITIES[domainId];
  if (!activities) return null;
  return activities.find((a) => a.id === activityId) || null;
}

export function getActivitiesForDomain(domainId) {
  return ACTIVITIES[domainId] || [];
}

export function getActivitiesByDifficulty(domainId, difficulty) {
  return getActivitiesForDomain(domainId).filter((a) => a.difficulty <= difficulty);
}
